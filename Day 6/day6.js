/*
  Some more...
        x = y  means	x = y
        x += y  means	x = x + y
        x -= y  means	x = x - y
        x *= y  means	x = x * y
        x /= y  means	x = x / y
        x %= y  means	x = x % y
        x **= y means	x = x ** y


  Some important ans confusing .. 

      [==]  equal to	[ x == 5]
      [===]  equal value and equal type	 [ x === 5 ]
      [!=	]  not equal	[ x != 5]
      [!==]  not equal value or not equal type	[ x !== 5 ]
*/


/* 
JavaScript Logical Operators  :- 

 1 => && {and Operator}  => Both condition should be true
 2 => || {Or Operator} => atleast one condtion should be true	  
 3 => ! {Not Operator}	 => value will reverse {True become's false and false becomes true}

*/